package java_1005;

public class Calculator {
    public int plus(int a, int b){
        return a + b;
    }

    public int minus(int a, int b){
        return a - b;
    }

    public int multiple(int a, int b){
        return a * b;
    }

    public double divide(double a, double b){
        return a / b;
    }

}
